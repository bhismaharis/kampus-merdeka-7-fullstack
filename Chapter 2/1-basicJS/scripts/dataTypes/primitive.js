// String
let address = "Yogyakarta";
console.log(address); // Yogyakarta

//  Number
const score = 0.1 + 0.2;
const isScoreValid = 0.3 == score;
console.log(score); // 0.30000000000000004
console.log(isScoreValid); // false

// Boolean
let isRain = true; // true atau false
console.log(isRain); // true

// Null
let nullable = null;
console.log(nullable); // null

// Undefined
var undefinableVar = undefined;
console.log(undefinableVar); // undefined

/*
    Notes:
    null, undefined is same. null is for variable that null or empty
    undefined is variable that has not defined and it is same to boolean false it we use it in condition

    Catatan:
    null, undefined itu sama. null adalah untuk variabel yang kosong
    undefined adalah variabel yang belum didefinisikan dan sama dengan boolean false jika kita gunakan dalam kondisi
*/