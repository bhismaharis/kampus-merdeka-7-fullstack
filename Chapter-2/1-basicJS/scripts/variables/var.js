// Var merupakan deklarasi paling lama yang digunakan dalam javascript. Var dapat diubah nilainya dan dideklarasikan ulang. Var memiliki scope global dan local.

var name = "Fariq"; // Deklarasi variabel dengan var (Declare)
console.log(name);

name = "Fariq Abdhe"; // Kita dapat mengubah nilai variabel
console.log(name);

var name = "Fariq Abdhe Manaf"; // Deklarasi ulang variabel (Redeclare)
console.log(name);
